// String basics

// Strings represent sequences of arbitrary characters.
// They're delineated by paired double-quotes.
"hello"

// Using println lets us print to the Console in a running iPhone app.
println("Hello, World!")

// Concatenation uses the same syntax as numeric addition, but it means
// something different, to combine two strings into one:
"Hello, " + "World!"

// Interpolation is the process of taking another value and inserting
// it into a string:
let age = 24
"I'm \(age) years old."

// You can also put whole expressions inside the interpolation syntax:
"I'm \(2015-1991) years old."

// These values can also be other Strings:
let name = "Toshi"
"My dog's name is \(name)."

